package org.example.topic0IntroductionToJavaAndOOP;

public class Ex02 {
    public static void main(String[] args) {
        int number = 49;
        System.out.println("El número es " + number);

        if (number % 2 == 0) {
            System.out.println("Even Number");  // número par
        } else {
            System.out.println("Odd Number");   // número impar
        }
        System.out.println("bye!");
    }
}
